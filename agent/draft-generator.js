import fs from 'fs';
import path from 'path';
import PDFDocument from 'pdfkit';
import { OpenAI } from 'openai';

const openai = new OpenAI({
  apiKey: process.env.GITHUB_TOKEN,
  baseURL: 'https://models.github.ai/inference',
});
const CHAT_MODEL = 'openai/gpt-4o-mini';
const DRAFTS_DIR = path.join(process.cwd(), 'docs', 'drafts');

function slugify(s) {
  return s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '').slice(0, 60);
}

/**
 * Renders the drafted content as a one-page PDF stub — same shape ingest.js
 * already knows how to read, so approval can hand it straight to ingestFile()
 * with zero changes to the ingestion pipeline.
 */
function renderPdf(filePath, { title, summary, body, permalink, hitCount }) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 64 });
    const stream = fs.createWriteStream(filePath);
    doc.pipe(stream);

    doc.fontSize(9).fillColor('#b45309').text('DRAFT — PENDING REVIEW', { continued: false });
    doc.moveDown(0.5);
    doc.fontSize(20).fillColor('#1a1a1a').text(title);
    doc.fontSize(9).fillColor('#6b6b6b').text(`Auto-generated from a resolved Slack thread · asked ${hitCount} time(s)`);
    doc.moveDown();
    doc.fontSize(11.5).fillColor('#333333').text(summary);
    doc.moveDown();
    doc.fontSize(10.5).fillColor('#222222').text(body, { align: 'left' });
    doc.moveDown(1.5);
    doc.fontSize(8.5).fillColor('#8a8a8a').text(`Provenance: ${permalink}`);
    doc.text('Generated by my-rag-bot gap-detect — review and approve/reject via Slack DM before treating as authoritative.');

    doc.end();
    stream.on('finish', resolve);
    stream.on('error', reject);
  });
}

/**
 * Drafts a doc stub from a gap question + the reply that resolved it, and
 * writes it to docs/drafts/ as a one-page PDF with the source thread linked
 * as provenance. Does NOT touch the live knowledge base — that only happens
 * on stakeholder approval (see listeners/actions/draft_approval.js).
 *
 * @param {{question: string, resolvingText: string, permalink: string, hitCount: number}} input
 * @returns {Promise<{slug: string, filePath: string, title: string, summary: string}>}
 */
export async function draftStub({ question, resolvingText, permalink, hitCount }) {
  const res = await openai.chat.completions.create({
    model: CHAT_MODEL,
    response_format: { type: 'json_object' },
    messages: [
      {
        role: 'system',
        content:
          'Draft a short internal doc stub from a question and the reply that answered it. ' +
          'Respond only with JSON: {"title": string, "summary": string, "body": string}. ' +
          'title: <=8 words. summary: one sentence. body: plain text, 2-4 short paragraphs, ' +
          'written as documentation (not as a recap of the conversation).',
      },
      {
        role: 'user',
        content: `Question asked ${hitCount} time(s): ${question}\n\nAnswer that resolved it:\n${resolvingText}`,
      },
    ],
  });

  const { title, summary, body } = JSON.parse(res.choices[0].message.content);
  const slug = slugify(title);

  fs.mkdirSync(DRAFTS_DIR, { recursive: true });
  const filePath = path.join(DRAFTS_DIR, `${slug}.pdf`);

  await renderPdf(filePath, { title, summary, body, permalink, hitCount });

  return { slug, filePath, title, summary };
}
